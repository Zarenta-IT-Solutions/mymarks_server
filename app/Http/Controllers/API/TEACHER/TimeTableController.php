     */
    public function index()
    {
        $query = Subject::select('name','class_id','class_time','code')->where('teacher_id',auth()->id())->with('class');
        if(\request()->has('search') && \request()->search!='null' && \request()->search!=null){
            $query->where('name','like','%'.\request()->search.'%');
        }
        return $query->latest()->paginate(\request()->perPage);
    }

